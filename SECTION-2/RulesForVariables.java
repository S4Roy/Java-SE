import java.lang.*;

class RulesForVariables
{
	public static void main(String args[])
	{
		int roll_number = 10;
		int _roll_number = 20;
		int $roll = 50;
		int rollNumber = 100;
		int roll_number$student = 101;
		float averageMarksOfClass = 90f;
		System.out.println("roll_number : " + roll_number);
		System.out.println("_roll_number : " + _roll_number);
		System.out.println("$roll : " + $roll);
		System.out.println("rollNumber : " + rollNumber);
		System.out.println("roll_number$student : " + roll_number$student);
		System.out.println("averageMarksOfClass : " + averageMarksOfClass);
	}
}
